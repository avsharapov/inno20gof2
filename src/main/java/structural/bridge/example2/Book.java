package structural.bridge.example2;

public class Book {
  private String author;
  private String title;
  private String contPage;
  private String year;

  public String getAuthor() {
    return author;
  }

  public void setAuthor(String author) {
    this.author = author;
  }

  public String getTitle() {
    return title;
  }

  public void setTitle(String title) {
    this.title = title;
  }

  public String getContPage() {
    return contPage;
  }

  public void setContPage(String contPage) {
    this.contPage = contPage;
  }

  public String getYear() {
    return year;
  }

  public void setYear(String year) {
    this.year = year;
  }
}
